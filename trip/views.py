# Fixed cost and efficiency values
PETROL_PRICE = 100  # INR per liter
FUEL_EFFICIENCY = {  # km per liter
    1: 40,  # 2-Wheeler
    2: 15   # 4-Wheeler
}

ACCOMMODATION_COSTS = {  # Cost per day (INR)
    'home_stay': 1000,
    'resort': 3000,
    'pg_hostel': 300,
    'Hotel': 1000,
    'Luxury_Hotel': 8000,
    'Guest_House': 1500,
    'Dormitory': 200,
    'Villa': 5000,
    'Boutique_Hotel':4000,

}

DAILY_FOOD_COST = 300
FASTTRACK_PRICE_PER_TOLL = 375  # Price per toll crossing

def home(request):
    if request.method == 'POST':
        # Get user inputs
        user_location = request.POST.get('user_location')
        destination = request.POST.get('destination')
        vehicle_type = int(request.POST.get('vehicle_type'))  # 1 for 2-Wheeler, 2 for 4-Wheeler
        accommodation_type = request.POST.get('accommodation_type')  # home_stay, resort, pg_hostal, large
        days_of_stay = int(request.POST.get('days_of_stay'))  # Number of days

        # Calculate the distance between user_location and destination
        user_coords = TAMIL_NADU_DISTRICTS.get(user_location)
        destination_coords = TAMIL_NADU_DISTRICTS.get(destination)

        if user_coords and destination_coords:
            user_lat_lon = (user_coords['lat'], user_coords['lon'])
            destination_lat_lon = (destination_coords['lat'], destination_coords['lon'])
            distance_km = geodesic(user_lat_lon, destination_lat_lon).km  # Calculate the distance

            # Calculate the travel cost
            fuel_efficiency = FUEL_EFFICIENCY[vehicle_type]
            fuel_cost = (distance_km // fuel_efficiency) * PETROL_PRICE

            # Calculate the number of tolls and FastTrack cost
            tolls = DISTRICT_TOLLS.get((user_location, destination), DISTRICT_TOLLS.get((destination, user_location), 0))

            fasttrack_cost = 0
            if vehicle_type == 2:  # 4-Wheeler
                fasttrack_cost = tolls * FASTTRACK_PRICE_PER_TOLL

            # Calculate accommodation and food costs
            accommodation_cost = ACCOMMODATION_COSTS[accommodation_type] * days_of_stay
            food_cost = DAILY_FOOD_COST * days_of_stay
            # Final total cost
            total_cost = fuel_cost + accommodation_cost + food_cost + fasttrack_cost

            context = {
                'travel_cost': total_cost,
                'distance': distance_km,
                'fuel_cost': fuel_cost,
                'fasttrack_cost': fasttrack_cost,
                'accommodation_cost': accommodation_cost,
                'food_cost': food_cost,
                'vehicle_type': "2-Wheeler" if vehicle_type == 1 else "4-Wheeler",
                'accommodation_type': accommodation_type.replace('_', ' ').capitalize(),
                'days_of_stay': days_of_stay,
                'user_location': user_location,
                'destination': destination,
                'tolls': tolls,
            }

            return render(request, 'result.html', context)

    return render(request, 'home.html')
